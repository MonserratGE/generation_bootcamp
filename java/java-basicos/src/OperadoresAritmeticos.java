
public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		//Suma
		int sum = 10 + 5;
		
		//Resta
		int rest = 200 - 50;
		
		//Multiplicacion
		int mul = 5 * 5;
		
		//División
		int div = 10 / 5;
		
		//Modulo
		int modulo = 5%2;
		
		int numero = 10;
		
		//Incremento
		numero++;
		++numero;
		
		// Decremento
		numero--;
		--numero;
		
		//numero = numero + 2
		numero += 2;
		
		//numero = numero - 10
		numero -= 10;
		
		//numero = numero * 6
		numero *= 6;
		
		// numero = numero / 3
		numero /= 3;
		
			
		
		System.out.println(sum);
		System.out.println(rest);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(modulo);
		
	}

}
