
public abstract class TipoDeDatos {
	
	public static void main(String[] arg) {
		
//		Tipos de datos primitivos
		byte numeroMasPquenho = 100;
		short numeroPequenho = 32000;
		int number = 54;
		long numeroGrande = 1000000;
		
		float numeroDecimal = 12114.23134f;
		double numeroDecimalGrande = 1515.151;
		
		boolean verdadero = true;
		boolean falso =  false;
		
		char letra = 'l';
		
//		Dato estructurado
		String nombre = "Monse";
		
		
		
		
		System.out.println(number);
		System.out.println(numeroMasPquenho);
		System.out.println(numeroPequenho);
		System.out.println(numeroGrande);
		
		System.out.println(numeroDecimal);
		System.out.println(numeroDecimalGrande);
		
		System.out.println(verdadero);
		System.out.println(falso);
		
		System.out.println(letra);
		System.out.println(nombre);
		
		
	}
}
